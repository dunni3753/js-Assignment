// Addition (+):
{
var sum1 = 5 + 3;    // 8
var sum2 = -7 + 10;   // 3
var sum3 = 2.5 + 1.5; // 4
var sum4 = 0 + 7;     // 7
var sum5 = 12 + (-5);  // 7
console.log(sum1,sum2,sum3,sum4,sum5);
}

// Subtraction (-):
{
var diff1 = 10 - 4;    // 6
var diff2 = 7 - (-3);  // 10
var diff3 = 3.5 - 1.5; // 2
var diff4 = 0 - 7;     // -7
var diff5 = -12 - (-5); // -7
console.log(diff1,diff2,diff3,diff4,diff5);
}

// Product (Multiplication (*)):
{
var prod1 = 3 * 6;    // 18
var prod2 = -2 * 5;   // -10
var prod3 = 2.5 * 4;  // 10
var prod4 = 0 * 7;    // 0
var prod5 = 12 * (-2); // -24
console.log(prod1,prod2,prod3,prod4
  ,prod5);
}

// Division (/):
{
var quo1 = 20 / 5;   // 4
var quo2 = -15 / 3;  // -5
var quo3 = 10 / 2.5; // 4
var quo4 = 7 / 0;    // Infinity
var quo5 = 12 / (-3); // -4
console.log(quo1,quo2,quo3
  ,quo4,quo5);
}
// Modulus (%):
{
var rem1 = 15 % 4;   // 3
var rem2 = -10 % 3;  // -1
var rem3 = 8.5 % 3;  // 2.5
var rem4 = 7 % 0;    // NaN (Not a Number)
var rem5 = -12 % 5;  // -2
console.log(rem1,rem2,rem3,rem4,rem5)
}

